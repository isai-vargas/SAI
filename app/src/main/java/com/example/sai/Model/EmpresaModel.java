package com.example.sai.Model;

public class EmpresaModel {

    private int codigo;
    private String nombre;
    private String nit;
    private String descripcion;
    private String direccion;
    private String departamento;
    private String telefono;
    private boolean monedas;
    private int catalogo;
    private String correo;
    private boolean consolida;
    private int id;
    private String establecimiento; // Puede ser null, usa String y maneja el valor null
    private String municipio;
    private int moneda;
    private String archivo;
    private String claveArchivo;
    private String felId;
    private String felKey;
    private int lista;

    // Getters y setters para cada campo

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNit() {
        return nit;
    }

    public void setNit(String nit) {
        this.nit = nit;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public boolean isMonedas() {
        return monedas;
    }

    public void setMonedas(boolean monedas) {
        this.monedas = monedas;
    }

    public int getCatalogo() {
        return catalogo;
    }

    public void setCatalogo(int catalogo) {
        this.catalogo = catalogo;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public boolean isConsolida() {
        return consolida;
    }

    public void setConsolida(boolean consolida) {
        this.consolida = consolida;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEstablecimiento() {
        return establecimiento;
    }

    public void setEstablecimiento(String establecimiento) {
        this.establecimiento = establecimiento;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public int getMoneda() {
        return moneda;
    }

    public void setMoneda(int moneda) {
        this.moneda = moneda;
    }

    public String getArchivo() {
        return archivo;
    }

    public void setArchivo(String archivo) {
        this.archivo = archivo;
    }

    public String getClaveArchivo() {
        return claveArchivo;
    }

    public void setClaveArchivo(String claveArchivo) {
        this.claveArchivo = claveArchivo;
    }

    public String getFelId() {
        return felId;
    }

    public void setFelId(String felId) {
        this.felId = felId;
    }

    public String getFelKey() {
        return felKey;
    }

    public void setFelKey(String felKey) {
        this.felKey = felKey;
    }

    public int getLista() {
        return lista;
    }

    public void setLista(int lista) {
        this.lista = lista;
    }

}
